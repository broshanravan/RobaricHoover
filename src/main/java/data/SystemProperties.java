package data;

public class SystemProperties {

    private String labelPrinterSpec;
    private String receiptPrinterSpec;
    private String dBLocation;

    public String getLabelPrinterSpec() {
        return labelPrinterSpec;
    }

    public void setLabelPrinterSpec(String labelPrinterSpec) {
        this.labelPrinterSpec = labelPrinterSpec;
    }

    public String getReceiptPrinterSpec() {
        return receiptPrinterSpec;
    }

    public void setReceiptPrinterSpec(String receiptPrinterSpec) {
        this.receiptPrinterSpec = receiptPrinterSpec;
    }

    public String getdBLocation() {
        return dBLocation;
    }

    public void setdBLocation(String dBLocation) {
        this.dBLocation = dBLocation;
    }
}
